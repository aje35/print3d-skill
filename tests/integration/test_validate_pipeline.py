class TestValidateGcodeWarnWithPETGTempsOnPLAProfile:
    """validate_gcode() returns WARN when PETG temps (240°C) are used against
    the PLA profile (max 220°C).

    A G-code with a 240°C hotend is above the PLA maximum; the validator
    must flag this as WARN (quality risk, not a physical impossibility).
    """

    @pytest.fixture()
    def petg_temps_gcode(self, tmp_path: Path) -> Path:
        """G-code with PETG-range temperatures (240°C hotend, 80°C bed)."""
        content = (
            "; minimal gcode with PETG temps\n"
            "M104 S240 ; PETG hotend\n"
            "M140 S80 ; PETG bed\n"
            "M190 S80\n"
            "M109 S240\n"
            "G28\n"
            "G1 Z0.2 F3000\n"
            "G1 X10 Y10 E1.0 F1500\n"
            "G1 X50 Y50 E3.0 F1500\n"
            "G1 E-1.0 F2700\n"
            "M107\n"
            "M84\n"
        )
        path = tmp_path / "petg_temps.gcode"
        path.write_text(content)
        return path

    def test_status_is_warn(self, petg_temps_gcode: Path):
        result = validate_gcode(str(petg_temps_gcode), material="PLA")
        assert result.status == ValidationStatus.WARN

    def test_warnings_list_is_non_empty(self, petg_temps_gcode: Path):
        result = validate_gcode(str(petg_temps_gcode), material="PLA")
        # result.warnings contains formatted "[check_name] message" strings
        assert len(result.warnings) > 0

    def test_no_hard_failures_present(self, petg_temps_gcode: Path):
        # Temperature mismatch is advisory (WARN), not a hard FAIL
        result = validate_gcode(str(petg_temps_gcode), material="PLA")
        assert result.failures == []

    def test_hotend_temperature_check_is_warn(self, petg_temps_gcode: Path):
        from print3d_skill.models.validate import CheckCategory, CheckSeverity

        result = validate_gcode(str(petg_temps_gcode), material="PLA")
        hotend_checks = [
            c
            for c in result.checks
            if c.category == CheckCategory.TEMPERATURE and c.name == "hotend_temperature"
        ]
        assert len(hotend_checks) == 1
        assert hotend_checks[0].severity == CheckSeverity.WARN

    def test_recommendation_references_pla_max_temp(self, petg_temps_gcode: Path):
        result = validate_gcode(str(petg_temps_gcode), material="PLA")
        # At least one recommendation should mention lowering to 220°C
        all_recs = " ".join(result.recommendations)
        assert "220" in all_recs


class TestValidateGcodeFailBuildVolumeExceeded:
    """validate_gcode() returns FAIL when print coordinates exceed the printer
    build volume.

    The generic_bowden profile has a 220x220mm footprint.  A G-code that
    moves to X=300 / Y=300 must trigger a hard FAIL on the build_volume check.
    """

    @pytest.fixture()
    def oversized_gcode(self, tmp_path: Path) -> Path:
        """G-code with moves that exceed a 220x220 build volume."""
        content = (
            "; oversized print\n"
            "M104 S215\n"
            "M140 S60\n"
            "M190 S60\n"
            "M109 S215\n"
            "G28\n"
            "G1 Z0.2 F3000\n"
            # Moves to X=300, Y=300 — well outside a 220×220mm bed
            "G1 X0 Y0 E0 F1500\n"
            "G1 X300 Y0 E5.0 F1500\n"
            "G1 X300 Y300 E10.0 F1500\n"
            "G1 X0 Y300 E15.0 F1500\n"
            "G1 X0 Y0 E20.0 F1500\n"
            "G1 E-1.0 F2700\n"
            "M107\n"
            "M84\n"
        )
        path = tmp_path / "oversized.gcode"
        path.write_text(content)
        return path

    def test_status_is_fail(self, oversized_gcode: Path):
        result = validate_gcode(str(oversized_gcode), printer="generic_bowden")
        assert result.status == ValidationStatus.FAIL

    def test_failures_list_is_non_empty(self, oversized_gcode: Path):
        result = validate_gcode(str(oversized_gcode), printer="generic_bowden")
        assert len(result.failures) > 0

    def test_build_volume_check_fails(self, oversized_gcode: Path):
        from print3d_skill.models.validate import CheckCategory, CheckSeverity

        result = validate_gcode(str(oversized_gcode), printer="generic_bowden")
        volume_checks = [
            c
            for c in result.checks
            if c.category == CheckCategory.DIMENSIONS and c.name == "build_volume"
        ]
