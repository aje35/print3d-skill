class TestParseGcodeErrorCases:
    """parse_gcode() raises the correct exceptions for invalid inputs."""

    def test_empty_file_raises_gcode_parse_error(self, gcode_fixtures_dir: Path):
        empty_path = gcode_fixtures_dir / "empty.gcode"
        with pytest.raises(GcodeParseError):
            parse_gcode(str(empty_path))

    def test_empty_file_error_message_mentions_empty(self, gcode_fixtures_dir: Path):
        empty_path = gcode_fixtures_dir / "empty.gcode"
        with pytest.raises(GcodeParseError, match="empty"):
            parse_gcode(str(empty_path))

    def test_non_gcode_extension_raises_unsupported_format_error(self, tmp_path: Path):
        stl_path = tmp_path / "model.stl"
        stl_path.write_text("solid fake\nendsolid\n")
        with pytest.raises(UnsupportedFormatError):
            parse_gcode(str(stl_path))

    def test_txt_extension_raises_unsupported_format_error(self, tmp_path: Path):
        txt_path = tmp_path / "notes.txt"
        txt_path.write_text("G28\nM104 S200\n")
        with pytest.raises(UnsupportedFormatError):
            parse_gcode(str(txt_path))

    def test_nonexistent_file_raises_file_not_found(self, tmp_path: Path):
        missing = tmp_path / "does_not_exist.gcode"
        with pytest.raises(FileNotFoundError):
            parse_gcode(str(missing))

    def test_unsupported_format_error_message_mentions_extension(self, tmp_path: Path):
        bad_path = tmp_path / "model.obj"
        bad_path.write_text("v 0 0 0\n")
        with pytest.raises(UnsupportedFormatError, match=r"\.gcode"):
            parse_gcode(str(bad_path))


